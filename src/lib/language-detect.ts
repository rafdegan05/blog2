/**
 * Detect the language of the given text using stopword frequency analysis.
 * Returns an ISO 639-1 language code (e.g. "en", "it", "es") or null if
 * the text is too short or no language could be reliably determined.
 *
 * @param text  Text to analyse (transcript, description, etc.)
 * @param candidateLangs  Restrict detection to a subset of languages (optional)
 */
export function detectLanguage(
  text: string,
  candidateLangs?: string[]
): { language: string; confidence: number } | null {
  if (!text || text.length < 20) return null;

  // Normalise: lowercase, strip timestamps, punctuation, numbers
  const cleaned = text
    .toLowerCase()
    .replace(/\d{1,2}:\d{2}(:\d{2})?([.,]\d+)?/g, " ") // timestamps
    .replace(/-->|WEBVTT/g, " ")
    .replace(/[^\p{L}\s]/gu, " ") // keep only letters + whitespace
    .replace(/\s+/g, " ")
    .trim();

  const words = cleaned.split(" ").filter((w) => w.length > 0);
  if (words.length < 5) return null;

  const totalWords = words.length;
  const wordSet = new Set(words);
  const wordFreq = new Map<string, number>();
  for (const w of words) {
    wordFreq.set(w, (wordFreq.get(w) || 0) + 1);
  }

  const langs = candidateLangs
    ? Object.entries(STOPWORDS).filter(([code]) => candidateLangs.includes(code))
    : Object.entries(STOPWORDS);

  let bestLang: string | null = null;
  let bestScore = 0;

  for (const [code, stopwords] of langs) {
    let hits = 0;
    for (const sw of stopwords) {
      if (wordSet.has(sw)) {
        hits += wordFreq.get(sw) || 0;
      }
    }
    const score = hits / totalWords;
    if (score > bestScore) {
      bestScore = score;
      bestLang = code;
    }
  }

  if (!bestLang || bestScore < 0.02) return null;

  return {
    language: bestLang,
    confidence: Math.min(bestScore * 5, 1), // normalise to 0-1 range
  };
}
